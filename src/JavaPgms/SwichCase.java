package JavaPgms;

public class SwichCase 
{
   public static void main(String[] args) 
   {
	  int tester=1;
	  
	  switch (tester) 
	  {
	  case 1 : System.out.println("anil");
	  break;
	  case 2 : System.out.println("nagaraj");
	  break;
	  case 3 : System.out.println("manoj");
	  break;
	  case 4 : System.out.println("srikanth");
	  break;
	  default : System.out.println("nothing");
	          
	  }
	  
   }
}
